    public void Registrar() {
        String sql = "insert into pelicula(NOMPEL,COSPEL,TIMPEL,FECPEL,GENPEL,CLASPEL,IDIPEL) values (?,?,?,?,?,?,?)";
        try {
            String genero = (String) jCboGenero.getSelectedItem();
            String clasificacion = (String) jCboClasificacion.getSelectedItem();
            String idioma = (String) jCboIdioma.getSelectedItem();
            PreparedStatement pst = (PreparedStatement) cn.prepareStatement(sql);
            pst.setString(1, txtNombre.getText());
            pst.setString(2, txtPrecio.getText());
            pst.setString(3, txtDuracion.getText());
            pst.setString(4, ((JTextField) jCalendar.getDateEditor().getUiComponent()).getText());
            pst.setString(5, genero);
            pst.setString(6, clasificacion);
            pst.setString(7, idioma);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Fecha Registrada");
            LimpiarTabla();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error de registro " + e.getMessage());
        }
    }
    void listar() {
        String sql = "SELECT *FROM pelicula";
        try {
            cn = con.getConnection();
            st = cn.createStatement();
            rs = st.executeQuery(sql);
            Object[] PELICULA = new Object[8];
            modelo = (DefaultTableModel) TablaDatos.getModel();
            while (rs.next()) {
                PELICULA[0] = rs.getInt("CODPEL");
                PELICULA[1] = rs.getString("NOMPEL");
                PELICULA[2] = rs.getString("COSPEL");
                PELICULA[3] = rs.getString("TIMPEL");
                PELICULA[4] = rs.getString("FECPEL");
                PELICULA[5] = rs.getString("GENPEL");
                PELICULA[6] = rs.getString("CLASPEL");
                PELICULA[7] = rs.getString("IDIPEL");
                modelo.addRow(PELICULA);
            }
            TablaDatos.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error de listado " + e.getMessage());
        }
    }
    void LimpiarTabla() {
        for (int i = 0; i < TablaDatos.getRowCount(); i++) {
            modelo.removeRow(i);
            i = i - 1;
        }
    }
