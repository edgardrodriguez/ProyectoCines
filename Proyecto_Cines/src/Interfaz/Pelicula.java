    private void TablaDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaDatosMouseClicked
        int fila = TablaDatos.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(null, "Vendedor no seleccionado");
        } else {
            id = Integer.parseInt((String) TablaDatos.getValueAt(fila, 0).toString());
            String nombre = (String) TablaDatos.getValueAt(fila, 1).toString();
            String precio = (String) TablaDatos.getValueAt(fila, 2).toString();
            String duracion =(String) TablaDatos.getValueAt(fila, 3).toString();
            String fecha=(String)  TablaDatos.getValueAt(fila, 4).toString();
            String genero= (String) TablaDatos.getValueAt(fila, 5).toString();
            String clasificacion= (String) TablaDatos.getValueAt(fila, 6).toString();
            String idioma= (String) TablaDatos.getValueAt(fila, 7).toString();
            txtID.setText("" + id);
            txtNombre.setText(nombre);
            txtPrecio.setText(precio);
            txtDuracion.setText(duracion);
            jCalendar.setDateFormatString(fecha); 
            jCboGenero.setSelectedItem(genero);
            jCboClasificacion.setSelectedItem(clasificacion);
            jCboIdioma.setSelectedItem(idioma);
        }
    }//GEN-LAST:event_TablaDatosMouseClicked

